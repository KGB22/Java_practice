public class For2 {
    public static void main(String[] args) {
        // 1부터 100까지 출력 후 거꾸로 100부터 1까지 출력하는 프로그램을 작성해보자.
        for(int a = 1; a <= 100; a++) {
            System.out.println(a);
        }
        for(int a = 100; a >= 1; a--) {
            System.out.println(a);
        }
        
    }

}
