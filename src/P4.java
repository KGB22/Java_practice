public class P4 {
    public static void main(String[] args) {
        int a = 50;
        int b = 5;

        //연산자
        //1. 산술연산자: + - * / %
        int result = 5 + 3;
        System.out.println(result);

        result = a + 7;
        System.out.println(result);

        result = a + b;
        System.out.println(result);

        result = a - b;
        System.out.println(result);

        result = a * b;
        System.out.println(result);

        result = a / b;
        System.out.println(result);

        int c = 5;
        int d = 2;

        result = c / d; // 정수 나누기
        System.out.println(result);

        result = c % d; // 나머지 연산
        System.out.println(result);



    }

}
