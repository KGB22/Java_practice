public class For9 {
    public static void main(String[] args) {
        //1부터 100까지 출력을 하고 난 다음에. 다시 거꾸로 100에서부터 1까지 출력을 하는 프로그램을 작성해보자.
        
        for(int a = 1; a <= 100; a++) {
            System.out.println(a);
        }
        for(int a = 100; a>=1; a--) {
            System.out.println(a);
        }
        
    }

}
